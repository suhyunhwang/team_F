package game.core;

/**
 * Scene 인터페이스
 * - 게임의 '한 화면(장면)'이 가져야 할 공통 동작을 정의함.
 * - 아침/점심/저녁/밤 스테이지가 모두 이 인터페이스를 구현하게 됨.
 */
public interface Scene {

    /** 장면이 처음 활성화될 때 한 번만 호출되는 초기화 메서드 */
    void init();

    /** 매 프레임마다 게임 로직을 갱신하는 메서드 (위치 이동, 충돌 판정 등) */
    void update();

    /** 현재 장면을 화면에 그리는 메서드 */
    void render();

    /** 키보드/마우스 등의 입력을 처리하는 메서드 */
    void handleInput();
}
